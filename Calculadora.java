public static class Calculadora {

	public static int suma(int op1, int op2) { return op1 + op2; }
	public static int resta(int op1, int op2) { return op1 - op2; }
	public static int multiplicacion(int op1, int op2) { return op1 * op2; }
	public static int division(int op1, int op2) { return op1 / op2; }

}

